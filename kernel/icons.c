/*============================================================================
 * Drawing Functions (alpha-aware: palette alpha < 0xFF blends with bg)
 *============================================================================*/

void icon_draw(int x, int y, const struct icon_sprite *icon, uint32_t bg)
{
    for (int iy = 0; iy < ICON_SIZE; iy++) {
        for (int ix = 0; ix < ICON_SIZE; ix++) {
            uint8_t idx = icon->data[iy][ix];
            if (idx == 0) {
                fb_put_pixel((uint32_t)(x + ix), (uint32_t)(y + iy), bg);
            } else {
                uint32_t color = icon->palette[idx];
                uint8_t alpha = (uint8_t)(color >> 24);
                if (alpha < 0xFF)
                    color = gfx_alpha_blend(color, bg, alpha);
                fb_put_pixel((uint32_t)(x + ix), (uint32_t)(y + iy), color);
            }
        }
    }
}

